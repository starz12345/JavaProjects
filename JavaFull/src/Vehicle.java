
public class Vehicle {
	
	private String name;
	private int gears;
	private int speed;
	private String steering;
	
	public Vehicle(String name, int gears, int speed, String steering) {
		this.name = name;
		this.gears = gears;
		this.speed = speed;
		this.steering = steering;
	}

	public String getName() {
		return name;
	}

	public int getGears() {
		return gears;
	}

	public int getSpeed() {
		return speed;
	}

	public String getSteering() {
		return steering;
	}
	
	
	
	
	
	
	

}
